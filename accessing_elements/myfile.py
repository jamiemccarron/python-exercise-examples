
class Math ():

    @staticmethod
    def add(x, y):
        return x + y

    @staticmethod
    def sub(x,y):
        return x-y

    @staticmethod
    def div(x,y):
        return x/y

    @staticmethod
    def mult(x,y):
        return x*y

    @staticmethod
    def square(x):
        return x*x

    @staticmethod
    def cubbed(x):
        return x*x*x
